    // Validar dados
    private boolean validaDados() {
        boolean vazio = this.number.getText().equals("") || this.name.getText().equals("") || this.curs.getText().equals("") || this.year.getText().equals("");
        
        if (vazio)
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor preencha todos os dados.", "Dados incompletos", 0);
        
        return !vazio;
    }
    
    private void adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarActionPerformed

        if (this.validaDados()) {
            String num = this.number.getText();
            String nome = this.name.getText();
            String curso = this.curs.getText();
            int ano = Integer.parseInt(this.year.getText());
           
            Aluno a = new Aluno(num,nome,curso,ano);
            this.cesium.addAluno(a);
            //javax.swing.JOptionPane.showMessageDialog("Aluno registado com sucesso.");
        }
    }//GEN-LAST:event_adicionarActionPerformed

